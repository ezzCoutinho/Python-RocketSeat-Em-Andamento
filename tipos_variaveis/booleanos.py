# Condição verdadeira
True

# Condição falsa
False

# Condicionais if, else if e else
if True: 
  print("Bloco será executado")
else: 
  print("Bloco não será executado")

# Operadores lógicos

# And "e"

if True and True: 
  print("Bloco será executado")

if True and False:
  print("Bloco não será executado")

# Or "ou"

if True or False:
  print("Bloco será executado")

if False or False:
  print("Este bloco não será executado")